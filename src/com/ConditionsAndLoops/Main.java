package com.ConditionsAndLoops;

public class Main {
    public static void main(String[] args) {
        int sal = 25400;

//        MULTIPLE IF-ELSE STATEMENT
        if (sal > 10000) {
            sal += 2000;
        } else if (sal > 20000) {
            sal += 3000;
        } else {
            sal += 1000;
        }

    }
}
